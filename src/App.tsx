import { Toaster } from "@/components/ui/toaster";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { TooltipProvider } from "@/components/ui/tooltip";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Routes, Route, Navigate } from "react-router-dom";
import Navbar from "@/components/Navbar";
import VideoAnalysisPage from "@/pages/VideoAnalysisPage";
import MarketingAnalysisPage from "@/pages/MarketingAnalysisPage";
import CommentAnalysisPage from "@/pages/CommentAnalysisPage";
import NotFound from "./pages/NotFound";

const queryClient = new QueryClient();

const App = () => (
  <QueryClientProvider client={queryClient}>
    <TooltipProvider>
      <Toaster />
      <Sonner />
      <BrowserRouter>
        <div className="min-h-screen bg-background">
          <Navbar />
          <main className="h-[calc(100vh-4rem)]">
            <Routes>
              <Route path="/" element={<Navigate to="/video-analysis" replace />} />
              <Route path="/video-analysis" element={<VideoAnalysisPage />} />
              <Route path="/marketing-analysis" element={<MarketingAnalysisPage />} />
              <Route path="/comment-analysis" element={<CommentAnalysisPage />} />
              <Route path="*" element={<NotFound />} />
            </Routes>
          </main>
        </div>
      </BrowserRouter>
    </TooltipProvider>
  </QueryClientProvider>
);

export default App;
